        public static string RandomFirstName()
        {
            return (Random.Next(2) == 0) ? RandomGirlName() : RandomBoyName();
        }

        public static string RandomFirstNameAndLastName()
        {
            return RandomFirstName() + " " + RandomLastName();
        }

        public static string RandomLastNameAndFirstName()
        {
            return RandomLastName() + " " + RandomFirstName();
        }

        public static string RandomGirlName()
        {
            return RandomString(GirlNamesWithWeights, SumWeightsGirlNames);
        }

        public static string RandomBoyName()
        {
            return RandomString(BoyNamesWithWeights, SumWeightsBoyNames);
        }

        public static string RandomLastName()
        {
            return RandomString(LastNamesWithWeights, SumWeightsLastNames);
        }

        private static string RandomString(Dictionary<string, int> stringWithWeight, int sumWeights)
        {
            int r = Random.Next(sumWeights);
            int countWeight = 0;
            foreach (KeyValuePair<string, int> entry in stringWithWeight)
            {
                countWeight += entry.Value;
                if (countWeight >= r)
                {
                    return entry.Key;
                }
            }

            throw new InvalidOperationException("Invalid state! Random algorithm not correct!");
        }


